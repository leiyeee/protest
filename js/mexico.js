    totals.forEach((d) => {
        if (totals_[d["action"]]) {
            totals_[d["action"]] += d.days;
        } else {
            totals_[d["action"]] = d.days;

        }
    })

    list = []
    for (var key in totals_) {
        list.push({
            action: key,
            days: totals_[key]
        });
    }

    list.sort((d1, d2) => d1.days - d2.days)
    totals = list;

    console.log(totals)

    var width = 180;
    var height = 180;
    var radius = Math.min(width, height) / 2;
    var donutWidth = 18;
