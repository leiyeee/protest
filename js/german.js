function ge() {

    totals_ = new Map()

    totals.forEach((d) => {
        if (totals_[d["action"]]) {
            totals_[d["action"]] += d.days;
        } else {
            totals_[d["action"]] = d.days;

        }
    })

    list = []
    for (var key in totals_) {
        list.push({
            action: key,
            days: totals_[key]
        });
    }

    list.sort((d1, d2) => d1.days - d2.days)
    totals = list;

    console.log(totals)

    var width = 180;
    var height = 180;
    var radius = Math.min(width, height) / 2;
    var donutWidth = 18;
    var color = d3.scaleOrdinal(["#9E0C3C", "#B01135", "#CD1B2D", "#E82322", "#FB291B", "#ff5555", "#ff8080", "#ffaaaa"]);

    var svg = d3.select('#donut6')
        .append('svg')
        .attr('width', width)
        .attr('height', height)
        .append('g')
        .attr('transform', 'translate(' + (width / 2) +
            ',' + (height / 2) + ')');

    var arc = d3.arc()
        .innerRadius(radius - donutWidth)
        .outerRadius(radius);

    var pie = d3.pie()
        .value(function (d) {
            return d.days;
        })
        .sort(null);

    var donutTip = d3.select("#donut6").append("div")
        .attr("class", "donut-tip")
        .style("color", "white")
        .style("opacity", 0)
        .style("position", "absolute")

    var path = svg.selectAll('path')
        .data(pie(totals))
        .enter()
        .append('path')
        .attr('d', arc)
        .attr('fill', function (d, i) {
            return color(d.data.action);
        })
        .attr('transform', 'translate(0, 0)')
        .on('mouseover', function (event, d) {

            console.log(this)

            /*  d3.select(this).transition()
      .attr('opacity', 0.5);
  donutTip.transition()
      .style("opacity", 1);*/
            svg.append("text")
                .attr("class", "donuttooltip")
                .text(d.data.days + " days")
                .attr("x", 0)
                .attr("y", 16)
                .style("font-size", 12)
                .style("fill", "white")
                .style("text-anchor", "middle")
                .style("alignment-baseline", "middle")

            /*           
                         donutTip
                             .html(d.data.days + " days")
                             .style("left", (this.getBBox().x + this.getBBox().width / 3 + width /2) + "px")
                             .style("top", (this.getBBox().y + this.getBBox().height / 3 + height/2) + "px");*/

        })
        .on('mouseout', function (event, d) {

            svg.selectAll(".donuttooltip").remove();

            /*d3.select(this).transition()
                .attr('opacity', 1);
                        donutTip.transition()
                            .style("opacity", 0);*/
        });



    var text = svg.append('text')
        .text('Germany')
        .attr("fill", "white") // set color
        .attr("class", "cname")
        .attr("x", 0)
        .attr("y", 0)
        .style("font-size", 18)
        .style("text-anchor", "middle")
        .style("alignment-baseline", "middle")

    console.log(text);

};

